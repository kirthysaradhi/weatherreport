package com.infodrive.weather.model;

/**
 * The Class Main.
 */
public class Main {

    /** The temp. */
    public int temp;

    /** The pressure. */
    public int pressure;

    /** The humidity. */
    public int humidity;

    /** The temp min. */
    public int temp_min;

    /** The temp max. */
    public int temp_max;

    /**
     * Gets the temp.
     *
     * @return the temp
     */
    public int getTemp() {
        return temp;
    }

    /**
     * Sets the temp.
     *
     * @param temp the new temp
     */
    public void setTemp(int temp) {
        this.temp = temp;
    }

    /**
     * Gets the pressure.
     *
     * @return the pressure
     */
    public int getPressure() {
        return pressure;
    }

    /**
     * Sets the pressure.
     *
     * @param pressure the new pressure
     */
    public void setPressure(int pressure) {
        this.pressure = pressure;
    }

    /**
     * Gets the humidity.
     *
     * @return the humidity
     */
    public int getHumidity() {
        return humidity;
    }

    /**
     * Sets the humidity.
     *
     * @param humidity the new humidity
     */
    public void setHumidity(int humidity) {
        this.humidity = humidity;
    }

    /**
     * Gets the temp min.
     *
     * @return the temp min
     */
    public int getTemp_min() {
        return temp_min;
    }

    /**
     * Sets the temp min.
     *
     * @param temp_min the new temp min
     */
    public void setTemp_min(int temp_min) {
        this.temp_min = temp_min;
    }

    /**
     * Gets the temp max.
     *
     * @return the temp max
     */
    public int getTemp_max() {
        return temp_max;
    }

    /**
     * Sets the temp max.
     *
     * @param temp_max the new temp max
     */
    public void setTemp_max(int temp_max) {
        this.temp_max = temp_max;
    }

}
