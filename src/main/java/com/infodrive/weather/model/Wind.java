package com.infodrive.weather.model;

/**
 * The Class Wind.
 */
public class Wind {

    /** The speed. */
    public int speed;

    /** The deg. */
    public int deg;

    /**
     * Gets the speed.
     *
     * @return the speed
     */
    public int getSpeed() {
        return speed;
    }

    /**
     * Sets the speed.
     *
     * @param speed the new speed
     */
    public void setSpeed(int speed) {
        this.speed = speed;
    }

    /**
     * Gets the deg.
     *
     * @return the deg
     */
    public int getDeg() {
        return deg;
    }

    /**
     * Sets the deg.
     *
     * @param deg the new deg
     */
    public void setDeg(int deg) {
        this.deg = deg;
    }

}
